interface I18nState {
  language: Language;
  setLanguage: (lang: Language) => void;
  t: (key: string) => string;
}

export const useI18n = create<I18nState>()(
  persist(
    (set, get) => ({
      language: "en",
      setLanguage: (language) => set({ language }),
      t: (key: string) => {
        const lang = get().language;
        return translations[lang]?.[key] || translations.en[key] || key;
      },
    }),
    {
      name: "language-storage",
    }
  )
);
